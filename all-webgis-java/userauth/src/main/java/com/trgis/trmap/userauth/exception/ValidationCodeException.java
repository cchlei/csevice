package com.trgis.trmap.userauth.exception;

/**
 * 系统验证用户合法性错误时抛出此异常
 * 
 * @author zhangqian
 *
 */
public class ValidationCodeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3381756033380421843L;

	public ValidationCodeException() {
		super();
	}

	public ValidationCodeException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ValidationCodeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ValidationCodeException(String message) {
		super(message);
	}

	public ValidationCodeException(Throwable cause) {
		super(cause);
	}

}
