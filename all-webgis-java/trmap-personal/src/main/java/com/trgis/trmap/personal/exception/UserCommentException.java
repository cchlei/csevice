package com.trgis.trmap.personal.exception;

public class UserCommentException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UserCommentException() {
		super();
		
	}

	public UserCommentException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public UserCommentException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public UserCommentException(String message) {
		super(message);
		
	}

	public UserCommentException(Throwable cause) {
		super(cause);
		
	}
}
