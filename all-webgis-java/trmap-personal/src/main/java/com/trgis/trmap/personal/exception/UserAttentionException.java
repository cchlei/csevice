package com.trgis.trmap.personal.exception;

/**
 * @ClassName: UserAttentionException
 * @author yanpeng
 * @date 2016年3月24日 下午3:40:15
 */
public class UserAttentionException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public UserAttentionException() {
		super();
		
	}

	public UserAttentionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public UserAttentionException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public UserAttentionException(String message) {
		super(message);
		
	}

	public UserAttentionException(Throwable cause) {
		super(cause);
		
	}
}
