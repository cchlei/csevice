package com.trgis.trmap.qtuser.exception;
/**
 * 个人地图矢量属性异常
 * @author doris
 * @date 2015-09-14
 */
public class MapGraphicsException extends RuntimeException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public MapGraphicsException() {
		super();
		
	}

	public MapGraphicsException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public MapGraphicsException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public MapGraphicsException(String message) {
		super(message);
		
	}

	public MapGraphicsException(Throwable cause) {
		super(cause);
		
	}

	

}
